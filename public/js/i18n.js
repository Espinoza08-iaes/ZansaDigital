const defaultLang = localStorage.getItem("lang") || document.documentElement.lang || "es";

function updateDOM() {
  // HTML translations (allows formatting within strings)
  // Supports two patterns:
  // 1) data-i18n-html="<key>"  (key stored in data-i18n-html)
  // 2) data-i18n="<key>" with boolean flag data-i18n-html (value "true" or empty)
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const htmlAttr = el.getAttribute("data-i18n-html");
    const key = htmlAttr && htmlAttr !== "true" ? htmlAttr : el.getAttribute("data-i18n");
    if (!key) return;
    const value = i18next.t(key);
    el.innerHTML = value;
  });

  // Text-only translations (skip those already handled as HTML)
  document.querySelectorAll("[data-i18n]:not([data-i18n-html])").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const value = i18next.t(key);
    el.textContent = value;
  });
  // Update placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    const value = i18next.t(key);
    el.setAttribute("placeholder", value);
  });
  document.documentElement.setAttribute("lang", i18next.language);
  // Toggle active state for language buttons
  document.querySelectorAll("[data-lang]").forEach((btn) => {
    const isActive = btn.getAttribute("data-lang") === i18next.language;
    btn.classList.toggle("bg-primary", isActive);
    btn.classList.toggle("text-white", isActive);
    btn.classList.toggle("text-primary", !isActive);
  });
}

window.changeLang = (lng) => {
  i18next.changeLanguage(lng).then(() => {
    localStorage.setItem("lang", lng);
    updateDOM();
    // Optional: toggle active button styles if buttons have data-lang
    document.querySelectorAll("[data-lang]").forEach((btn) => {
      const isActive = btn.getAttribute("data-lang") === lng;
      btn.classList.toggle("bg-primary", isActive);
      btn.classList.toggle("text-white", isActive);
      btn.classList.toggle("text-primary", !isActive);
    });
  });
};

document.addEventListener("DOMContentLoaded", () => {
  i18next
    .init({ lng: defaultLang, fallbackLng: "es", resources })
    .then(updateDOM);
});
